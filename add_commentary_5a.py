def main():
    bak = SRC.with_suffix(".json.bak")
    if not bak.exists():
        shutil.copy2(SRC, bak)
    data = json.loads(SRC.read_text(encoding="utf-8"))
    poems = data["poems"] if isinstance(data, dict) and "poems" in data else data
    by_id = {p["id"]: p for p in poems}
    updated = 0
    for pid, comm in COMMENTARY.items():
        p = by_id[pid]
        if p.get("notes") or p.get("background"):
            print(f"- id={pid} 已含讲解，跳过"); continue
        p["notes"] = comm["notes"]
        p["background"] = comm["background"]
        updated += 1
        print(f"✓ id={pid:>3} {p['author']}《{p['title']}》 注{len(comm['notes'])}条")
    total_with = sum(1 for p in poems if p.get("notes") or p.get("background"))
    print(f"\n本批(上)更新 {updated} 首；当前含讲解共 {total_with}/{len(poems)} 首")
