def main():
    if not SRC.exists():
        raise SystemExit(f"找不到 {SRC}")
    # 备份
    bak = SRC.with_suffix(".json.bak")
    if not bak.exists():
        shutil.copy2(SRC, bak)
        print(f"✓ 已备份 → {bak.name}")
    else:
        print(f"- 备份已存在 → {bak.name}（跳过）")

    data = json.loads(SRC.read_text(encoding="utf-8"))
    poems = data["poems"] if isinstance(data, dict) and "poems" in data else data
    by_id = {p["id"]: p for p in poems}

    updated = 0
    for pid, comm in COMMENTARY.items():
        if pid not in by_id:
            print(f"✗ 未找到 id={pid}（{comm.get('background','')[:20]}…）")
            continue
        p = by_id[pid]
        # 幂等：保留已有讲解（除非 --force）
        if p.get("notes") or p.get("background"):
            print(f"- id={pid} 已含讲解，跳过")
            continue
        p["notes"] = comm["notes"]
        p["background"] = comm["background"]
        updated += 1
        print(f"✓ id={pid:>3} {p['author']}《{p['title']}》 注{len(comm['notes'])}条 + 背景")

    # 统计
    total_with = sum(1 for p in poems if p.get("notes") or p.get("background"))
    print(f"\n本次更新 {updated} 首；含讲解共 {total_with}/{len(poems)} 首")

    SRC.write_text(
        json.dumps(data, ensure_ascii=False, indent=2),
        encoding="utf-8"
    )
    print(f"✓ 已写入 {SRC.name}")

if __name__ == "__main__":
    main()
